#4.1 Conditional Statements

# Run the following code:

n = 4   # n = 0 to 4
seasons = ["spring", "summer", "fall", "winter", "?"]
season = seasons[n]

if season == 'spring':
    print('plant the garden!')           # indentation should be 4 spaces
elif season == 'summer':
    print('water the garden!')
elif season == 'fall':
    print('harvest the garden!')
elif season == 'winter':
    print('stay indoors!')
else:
    print('unrecognized season')


